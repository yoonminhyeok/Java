package variable;

public class Var5 {
    public static void main(String[] args) {
        
        int a;  //변수 선언
        a=1;  // 변수 초기화
        System.out.println(a); // a 출력

        int b=2; // 변수 선언 및 초기화
        System.out.println(b); // b 출력

        int c=3, d=4;  // 여러 변수의 선언 및 초기화

        System.out.println(c);  // c 출력
        System.out.println(d);  // d 출력

    }
}
