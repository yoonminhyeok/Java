package operator;

public class Operator1 {
    public static void main(String[] args) {

        int a=5;
        int b=2;

        int sum= a+b;
        System.out.println("a+b =" +sum);

        int diff=a-b;

        System.out.println("a-b =" +diff);

        int mul=a*b;

        System.out.println("a*b =" +mul);

        int div=a/b;

        System.out.println("a/b =" +div);

        int mod=a%b;

        System.out.println("a%b =" +mod);
    }
}
